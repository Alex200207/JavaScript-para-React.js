//Repaso sobre variables con Let y const 

/*JavaScript es un lenguaje dinamico es decir no es necesario definir el tipo de la variable como los son
otros lenguajes como lo son c# entre otros.. */

//empezamos con let
//estas varibles no pueden empezar con numeros o caracteres especiales
let nombre = "Juan";//con let puedes iniciar un valor y luego cambiarlo tambien puedes inicarla sin nignun valor
nombre = "Pedro";

console.log(nombre);

//const 
//const es una variable que no puede cambiar su valor una vez que se le asigna un valor
/*utilizaras mas const que let  */
const apellido = "Perez";
//apellido = "Gomez"; //esto no se puede hacer
